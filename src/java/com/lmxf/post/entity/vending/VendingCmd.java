package com.lmxf.post.entity.vending;

/**
 * 售货机命令信息
 * @author Administrator
 *
 */
public class VendingCmd {

	private String logid;		//记录编号
	private String cmdId;		//命令编号
	private String cmdCode;		//命令对象编号
	private String cmdType;		//命令类型 01:售货机 02:商品 03: 订单
	private String cmdLazy;		//命令及时性
	private String cmd;			//命令类型
	private String cont;		//命令内容	
	private String createTime;	//创建时间
	private String state;		//执行状态0:执行中 1:执行成功 2:执行失败
	private String stateTime;	//状态时间
	private String result;		//执行结果
	private String corpId;		//托管公司编号

	private String siteId;
	
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	public String getLogid() {
		return logid;
	}
	public void setLogid(String logid) {
		this.logid = logid;
	}
	public String getCmdId() {
		return cmdId;
	}
	public void setCmdId(String cmdId) {
		this.cmdId = cmdId;
	}
	public String getCmdCode() {
		return cmdCode;
	}
	public void setCmdCode(String cmdCode) {
		this.cmdCode = cmdCode;
	}
	public String getCmdType() {
		return cmdType;
	}
	public void setCmdType(String cmdType) {
		this.cmdType = cmdType;
	}
	public String getCmdLazy() {
		return cmdLazy;
	}
	public void setCmdLazy(String cmdLazy) {
		this.cmdLazy = cmdLazy;
	}
	public String getCmd() {
		return cmd;
	}
	public void setCmd(String cmd) {
		this.cmd = cmd;
	}
	public String getCont() {
		return cont;
	}
	public void setCont(String cont) {
		this.cont = cont;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getStateTime() {
		return stateTime;
	}
	public void setStateTime(String stateTime) {
		this.stateTime = stateTime;
	}
	public String getResult() {
		return result;
	}
	public void setResult(String result) {
		this.result = result;
	}
	public String getCorpId() {
		return corpId;
	}
	public void setCorpId(String corpId) {
		this.corpId = corpId;
	}
	
}
