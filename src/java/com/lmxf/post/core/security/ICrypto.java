package com.lmxf.post.core.security;
 
public interface ICrypto {
	/**
	 * @param plain
	 * @return
	 */
	public	String	encrypt(String plain) throws Exception ;

	/**
	 * 
	 * @param plain
	 * @param passwd
	 * @return
	 * @throws Exception
	 */
	public	String	encrypt(String plain,String passwd) throws Exception ;
	/**
	 * @param cipher
	 * @return
	 */
	public	String	decrypt(String cipher) throws Exception ;
	
	/**
	 * 
	 * @param cipher
	 * @param passwd
	 * @return
	 * @throws Exception
	 */
	public	String	decrypt(String cipher,String passwd) throws Exception ;
	
}
